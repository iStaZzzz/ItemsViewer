import UIKit


class Fonts {
    
    func submitFont() -> UIFont { return UIFont.boldSystemFont(ofSize: 16) }
    
    func titleFont() -> UIFont { return UIFont.boldSystemFont(ofSize: 18) }
    
    func descriptionFont() -> UIFont { return UIFont.systemFont(ofSize: 15) }
}
